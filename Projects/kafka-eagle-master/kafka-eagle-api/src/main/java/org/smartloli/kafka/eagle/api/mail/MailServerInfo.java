package org.smartloli.kafka.eagle.api.mail;

import org.smartloli.kafka.eagle.common.protocol.BaseProtocol;

/**
 * Defined mail server address.
 *
 * @author smartloli.
 * <p>
 * Created by Nov 02, 2020
 */
public class MailServerInfo extends BaseProtocol {

    private String host;
    private String port;
    private String sa;
    private String username;
    private String password;
    private boolean enableSsl = false;

    public boolean isEnableSsl() {
        return enableSsl;
    }

    public void setEnableSsl(boolean enableSsl) {
        this.enableSsl = enableSsl;
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

    public String getSa() {
        return sa;
    }

    public void setSa(String sa) {
        this.sa = sa;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
